// current:要转换的电流， current_scale:电流范围(+, -), pulse_scale: pwm占空比范围，精度
uint32_t calculate_current_to_pwm(float current, float current_scale, uint32_t pulse_scale)
{
	uint32_t output_pulse = 0;

	float current_pulse_relation = 0;
	
	current_pulse_relation = (pulse_scale/2.0)/current_scale; // 电流转占空比，系数
	
	output_pulse = (uint32_t)((pulse_scale/2.0) + current*current_pulse_relation);

	
	return output_pulse;
	
}

void sac6010_set_output(char address, float current)
{

	uint16_t crc;
	int32_t current_data = (int32_t)(current*1000);
	//uint16_t crc;
	uint8_t send_list[13];


	send_list[0] = address;
	send_list[1] = 0x10;
	send_list[2] = 0x20;
	send_list[3] = 0x30;
	send_list[4] = 0x00;
	send_list[5] = 0x02;
	send_list[6] = 0x04;
	send_list[7] = (current_data >> 24) & 0XFF;
	send_list[8] = (current_data >> 16) & 0XFF;
	send_list[9] = (current_data >> 8) & 0XFF;
	send_list[10]= (current_data) & 0XFF;

	crc = crc16((uint8_t *)send_list, 11);

	send_list[11]= (crc >> 8) & 0XFF;
	send_list[12]= (crc) & 0XFF;
	
	HAL_GPIO_WritePin(GPIOA, GPIO_PIN_8, GPIO_PIN_SET);
	//HAL_UART_Transmit_DMA(&huart1, (uint8_t *)send_list, sizeof(send_list));
	HAL_UART_Transmit(&huart1, (uint8_t *)send_list, sizeof(send_list),1500);
	HAL_GPIO_WritePin(GPIOA, GPIO_PIN_8, GPIO_PIN_RESET);
}

