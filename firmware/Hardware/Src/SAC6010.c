/* 接收来自上位机的数据 */




void sac6010_read_open_output(uint8_t* send_list)
{

	uint16_t crc;

	crc = crc16((uint8_t *)send_list, 6);
	
	if((send_list[6] == ((crc >> 8) & 0XFF)) && (send_list[7] == ((crc) & 0XFF)))
	{
		coils_power = 1;
	}
}

void sac6010_read_close_output(uint8_t* send_list)
{

	uint16_t crc;

	crc = crc16((uint8_t *)send_list, 6);
	
	if((send_list[6] == ((crc >> 8) & 0XFF)) && (send_list[7] == ((crc) & 0XFF)))
	{
		coils_power = 0;
	}

}

void sac6010_read_set_output(uint8_t* send_list)
{

	uint16_t crc;
	float current = 0;
	
	uint8_t address = send_list[0];
	
	int32_t current_data = ((int32_t)send_list[7] << 24) | ((int32_t)send_list[8] << 16) | ((int32_t)send_list[9] << 8) | ((int32_t)send_list[10]);


	crc = crc16((uint8_t *)send_list, 11);

	if((send_list[11] == ((crc >> 8) & 0XFF)) && (send_list[12] == ((crc) & 0XFF)))
	{
		current = current_data/1000.0f;
		coils_current[address-1] = current;
	}
	
}


void sac6010_read_set_multi__output(uint8_t* send_list)
{

	uint16_t crc;
	int32_t current_data[8];
	
	
	uint8_t address = send_list[0];
	
	current_data[0] = ((int32_t)send_list[7] << 24) | ((int32_t)send_list[8] << 16) | ((int32_t)send_list[9] << 8) | ((int32_t)send_list[10]);
	current_data[1] = ((int32_t)send_list[11] << 24) | ((int32_t)send_list[12] << 16) | ((int32_t)send_list[13] << 8) | ((int32_t)send_list[14]);
	current_data[2] = ((int32_t)send_list[15] << 24) | ((int32_t)send_list[16] << 16) | ((int32_t)send_list[17] << 8) | ((int32_t)send_list[18]);
	current_data[3] = ((int32_t)send_list[19] << 24) | ((int32_t)send_list[20] << 16) | ((int32_t)send_list[21] << 8) | ((int32_t)send_list[22]);
	current_data[4] = ((int32_t)send_list[23] << 24) | ((int32_t)send_list[24] << 16) | ((int32_t)send_list[25] << 8) | ((int32_t)send_list[26]);
	current_data[5] = ((int32_t)send_list[27] << 24) | ((int32_t)send_list[28] << 16) | ((int32_t)send_list[29] << 8) | ((int32_t)send_list[30]);
	current_data[6] = ((int32_t)send_list[31] << 24) | ((int32_t)send_list[32] << 16) | ((int32_t)send_list[33] << 8) | ((int32_t)send_list[34]);
	current_data[7] = ((int32_t)send_list[35] << 24) | ((int32_t)send_list[36] << 16) | ((int32_t)send_list[37] << 8) | ((int32_t)send_list[38]);


	crc = crc16((uint8_t *)send_list, 39);

	if((send_list[39] == ((crc >> 8) & 0XFF)) && (send_list[40] == ((crc) & 0XFF)))
	{
		for(int i=0;i<8;i++)
		{
			coils_current[i] = current_data[i]/1000.0f;
		}
	}
}


int cnt = 0;

void sac6010_read_from_pc(uint8_t* send_list)
{
	cnt +=1;
	
	if(send_list[1] == 0x06 && send_list[2] == 0x60 && send_list[3] == 0x40 && send_list[4] == 0x00 && send_list[5] == 0xff)
	{
		sac6010_read_open_output(send_list);
	}
	else if(send_list[1] == 0x06 && send_list[2] == 0x60 && send_list[3] == 0x40 && send_list[4] == 0x00 && send_list[5] == 0x00)
	{
		sac6010_read_close_output(send_list);
	}
	else if(send_list[1] == 0x10 && send_list[2] == 0x20 && send_list[3] == 0x30 && send_list[4] == 0x00 && send_list[5] == 0x02 && send_list[6] == 0x04)
	{
		sac6010_read_set_output(send_list);
	}
	else if(send_list[1] == 0x10 && send_list[2] == 0x20 && send_list[3] == 0x30 && send_list[4] == 0x00 && send_list[5] == 0x02 && send_list[6] == 0x05)
	{
		sac6010_read_set_multi__output(send_list);
	}	
}




